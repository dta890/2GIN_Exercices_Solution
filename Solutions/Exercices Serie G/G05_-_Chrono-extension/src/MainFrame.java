private void toggleViewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toggleViewButtonActionPerformed
    int time = chronometer.getTime(); //mémoriser le temps actuel
    if (toggleViewButton.getText().indexOf("Analog") > -1)  //créer le nouveau chrono
    {
        toggleViewButton.setText("Digital View");
        chronometer = new AnalogChronometer();
    }
    else
    {
        toggleViewButton.setText("Analog View");
        chronometer = new Chronometer();
    }
    chronometer.setTime(time);  //reprendre le temps mémorisé
    drawPanel.setChronometer(chronometer); //actualiser le chrono dans drawPanel!!
    repaint(); 
}//GEN-LAST:event_toggleViewButtonActionPerformed

