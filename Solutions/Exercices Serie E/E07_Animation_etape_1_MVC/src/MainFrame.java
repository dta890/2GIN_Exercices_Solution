    private void stepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stepButtonActionPerformed
        if(xStep > 0  &&  ball.getX()+ball.getRadius()+xStep > drawPanel.getWidth() ||
           xStep < 0  &&  ball.getX()-ball.getRadius()+xStep < 0)
        {
            xStep = -xStep;  //changer de direction
        }
        ball.setX(ball.getX()+xStep); //avancer
        drawPanel.repaint();
    }//GEN-LAST:event_stepButtonActionPerformed

    private void startStopButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startStopButtonActionPerformed
        if (startStopButton.getText().indexOf("Start") >= 0)
        {
            timer.start();
            startStopButton.setText("Stop");
        }
        else
        {
            timer.stop();
            startStopButton.setText("Start");
        }
    }//GEN-LAST:event_startStopButtonActionPerformed

