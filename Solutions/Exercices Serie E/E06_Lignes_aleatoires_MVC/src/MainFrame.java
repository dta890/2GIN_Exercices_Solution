    private void stepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stepButtonActionPerformed
        Line l = new Line((int) (Math.random() * drawPanel.getWidth()),
                (int) (Math.random() * drawPanel.getHeight()),
                (int) (Math.random() * drawPanel.getWidth()),
                (int) (Math.random() * drawPanel.getHeight()));
        l.setColor(new Color((int) (Math.random() * 256),
                (int) (Math.random() * 256),
                (int) (Math.random() * 256)));
        lines.add(l);
        drawPanel.repaint();
    }//GEN-LAST:event_stepButtonActionPerformed

    private void timerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timerButtonActionPerformed
        if (timer.isRunning()) {
            timer.stop();
            timerButton.setText("Start timer");
        } else {
            timer.start();
            timerButton.setText("Stop timer");
        }
    }//GEN-LAST:event_timerButtonActionPerformed

