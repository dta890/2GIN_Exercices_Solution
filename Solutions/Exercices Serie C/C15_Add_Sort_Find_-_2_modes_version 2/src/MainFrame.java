    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        participants.add(new Participant(firstnameTextField.getText(),
                nameTextField.getText(),
                Integer.valueOf(ageTextField.getText())));
        clearTextFields();
        updateView();
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if (viewerList.getSelectedIndex() > -1) //si un élément est sélectionné
        {
            participants.remove(viewerList.getSelectedIndex());
            clearTextFields();
            updateView();
        }
    }//GEN-LAST:event_removeButtonActionPerformed

private void searchNameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchNameButtonActionPerformed
    updateView();  // pour effacer le marquage d'une ligne sélectionnée précédemment
    int i;
    if (nameRadioButton.isSelected()) {
        i = participants.search(nameTextField.getText());
    } else {
        i = participants.search(ageTextField.getText());
    }
    viewerList.setSelectedIndex(i);
}//GEN-LAST:event_searchNameButtonActionPerformed

