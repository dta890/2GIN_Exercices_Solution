    /**
     * Affiche les données de la liste des participants dans viewerList et
     * affiche les participants les plus âgés ou les plus jeunes dans les champs
     * prévus. Evite les erreurs si la liste est vide.
     */
    private void updateView() {
        viewerList.setListData(participants.toArray());
        if (participants.getEldest() != null) //null si la liste est vide
        {
            eldestLabel.setText(participants.getEldest().toString());
        }
        if (participants.getYoungest() != null) //null si la liste est vide
        {
            youngestLabel.setText(participants.getYoungest().toString());
        }
    }

    /**
     * Efface les champs de données
     */
