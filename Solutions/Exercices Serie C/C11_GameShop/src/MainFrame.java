    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        Game newGame = new Game(    titleTextField.getText(),
                                    categoryTextField.getText(),
                                    systemTextField.getText(),
                                    Integer.valueOf(uskTextField.getText()),
                                    Double.valueOf(priceTextField.getText()) );
        gameStock.add(newGame);
        updateView();
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (gamesList.getSelectedIndex()!=-1)
            gameStock.remove(gamesList.getSelectedIndex());
        updateView();
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void gamesListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_gamesListValueChanged
       if (gamesList.getSelectedIndex()!=-1) {
            Game selGame = gameStock.get(gamesList.getSelectedIndex());
            titleTextField.setText(selGame.getTitle());
            categoryTextField.setText(selGame.getCategory());
            systemTextField.setText(selGame.getSystem());
            uskTextField.setText(String.valueOf(selGame.getUsk()));
            priceTextField.setText(String.valueOf(selGame.getPrice()));
        }

    }//GEN-LAST:event_gamesListValueChanged

